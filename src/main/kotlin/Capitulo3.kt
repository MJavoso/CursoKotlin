fun main() {
    // Operadores aritméticos
    val x = 8
    val y = 3

    println(x + y)
    println(x - y)
    println(x * y)
    println(x / y)

    println(x % y)

    var a = 10
    println(++a)
    println(--a)

    a %= 6

    // Operadores lógicos y de comparación
    println(x == y)
    println(x != y)

    println(x === y)
    println(x !== y)

    println(x >= y)
    println(x <= y)
    println(x > y)
    println(x < y)

    val booleano = true
    println(booleano)
    println(!booleano)
}